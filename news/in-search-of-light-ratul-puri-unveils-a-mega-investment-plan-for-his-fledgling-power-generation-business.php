			<h1>In Search of Light unveils a mega investment plan for his fledgling power generation business</h1>
			
			<p>
			
				Hindustan Powerprojects Pvt. Ltd (HPPPL) came to be with its Chairman, Ratul Puri, looking for an image makeover for the company, which was earlier known as Moser Baer Power Projects. Moser Baer, founded in 1983 by his father Deepak Puri, became a household name for CDs, DVDs and rewritable discs. However, by 2008 the company had forayed into a different segment, namely power generation. Five years hence, Ratul Puri gave the company its new name - Hindustan Powerprojects Pvt. Ltd. "We are building a new identity," he says.</p><p>
HPPPL is not a subsidiary of Moser Baer, and his father does not hold any shares in the company (about 65 per cent of the company is owned by Puri an his children; the rest is with institutional investors). After stepping down from Moser Baer’s board, Puri has focused on building the power generation business, which he believes will grow exponentially in the future. </p><p>
Speaking to Business Today on the side lines of India Today’s conclave in New Delhi, he shared that India’s power sector must invest about a trillion dollars to fill the deficit in the next 20 years. His company will focus on coal thermal, hydro and solar power plants. While there are already big initiatives on the solar and coal front, Puri plans to invest Rs 32,000 crore into HPPPL by 2016, of which Rs 13,500 crore has already been utilised to increase HPPPL’s power generation capacity to six gigawatts (4 GW from thermal, 1.5 GW from solar and the rest from hydro). This step is likely to place the company among the top five power producers in India.</p><p>
Puri says he does not want to depend solely on thermal power to avoid the shortage of coal in India or depend on imported coal. HPPPL began solar power generation in 2010-11 and had an EBITDA earning of Rs 500 crore from its solar business in the year 2012-13. With HPPPL’s thermal power plant in Madhya Pradesh slated to undergo its first and second phases of commission in the coming future, the company hopes to generate EBITDA of over Rs 2500 crore. </p><p>
With a 25-year power purchase agreement with the Uttar Pradesh Power Corporation already under the company’s wing, Puri is happy with how his plans are taking shape on this long journey.</p><p>

