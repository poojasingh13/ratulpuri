			<h1>How corporates have been planning for multifarious range of possible post-election scenarios</h1>
	
			<p>
				While corporate India closely watched how the recently held elections in the country panned out, it has also resulted in serious planning and discussions among various sectors on the impact of new government and policies. Leaders across industries shared how their companies brace the future. </p><p>
While the RGP Group Chairman Harsh Goenka inducted the subject of elections into the company’s budgeting and planning meetings, insurance sector’s John Holden, managing director, Canara HSBC Oriental Bank of Commerce Life Insurance Company included scenario planning in the company’s annual planning meetings. </p><p>
Scenario planning has been around since 1960s since Shell introduced it as an important part of its planning exercise, as political and regulatory issues on a global scale matter to the energy industry. Today, the company still puts out its ‘lens scenarios’, which are based on alternative forecasts of energy usage.</p><p>
Another company that needs to plan for long term is Hindustan Powerprojects. Chairman Ratul Puri is keeping tabs on possible opportunities as well as risks that are involved with alternative post-election developments as the company makes sure to cover its bets by getting into thermal, hydro and solar power generation. Puri shares that the company has planned for the necessary actions they would take in light of either a strong or weak government. According to him, a strong government would mean acquisition of distressed assets while weak government will be a time to build cash reserves, and the going on projects could be slow.</p><p>
Estimating a 1.5 per cent GDP growth difference between a strong and weak government, Puri, who holds mathematics degree from Carnegie Mellon, shared that he uses a decision tree analysis to plan for uncertainties, which differs slightly from scenario planning in that it assigns probabilities to various futures. </p><p>
At BCG, scenario-planning exercises encourage clients to imagine extreme scenarios, while in the FMCG sector, which can be affected through people’s views on governance, Godrej Consumer Products has left its annual scenario planning exercise options open for now. The automobile industry, which is closely correlated to economic growth, is also looking closely at the new government to ease their situation. </p><p>
Though no company allows its entire business plan to depend on government’s policy, companies across industries await how the change in governance will affect technological developments, microeconomic trends, consumer behaviour, competitive landscape and the environmental resources. </p><p>
