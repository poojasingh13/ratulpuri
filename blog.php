<b>Agenda before the new government: Serious challenges that we should overcome.</b><br/>
			Not getting timely approvals or frequent change in the policies and guidelines are proving to be one of the big hurdles in front of the India power sector. In fact there are multitudes of approvals needed, sometimes from the same department more than once, for the same project . This results in huge losses and crucial management bandwidth being wasted. If the entire approval process could be streamlined into a time bound clearance mechanism, it would aid the industry significantly.
Other challenges which the energy sector is facing is acute shortage of raw material which is coal or gas, high interest rates, challenges associated with land acquisition, health of SEBs which should be taken due care off. Changes in sovereign laws of countries, like Indonesia which has pushed up import prices of coal is also another challenge the sector is facing.
There is a strong buzz that the government is planning to revoke the fuel supply assurances given to coal-based power projects that have failed to meet their commercial production deadline. However, there are various reasons which are beyond the control of the developers. This issue must be looked upon by the government before taking a final call.

