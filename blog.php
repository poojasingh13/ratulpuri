<b>Challenges Before Indian Energy Sector</b><br/>
			Having experienced near double digit growth over the last decade, India now faces the challenging task of meeting the aspirations of young India. I believe, that the expectations – employment, better life – can only be met with solving the energy deficiency in the country.
Thanks to numerous policy reforms over the past 20 years, the Indian energy sector has seen a shift towards addressing the needs of the consumer. However, the Indian energy sector today is facing a major dichotomy – one hand you have able and willing consumers who are ready to pay for the electricity and on other hand you a well equipped power generators willing to fulfil the demand. The inability of discoms to connect the consumers with the buyers has resulted in many thermal power plants lying idle. Adding to this, the long developmental cycle for conventional energy (7-10 years) and many other challenges has meant there will be only very limited incremental conventional power projects getting commissioned post 2017. All the while the energy requirement of Country will grow in significant proportion.


