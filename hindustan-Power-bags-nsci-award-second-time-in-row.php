				The thermal arm of Hindustan Powerprojects, Hindustan Thermalprojects, was awarded the National Safety Council Award (NSCI) by the labour ministry in recognition of the firm’s initiatives towards implementing effective occupational safety and health practices at its Anuppur unit in Madhya Pradesh. This is the second consecutive time that the plant has bagged the award. Hindustan Powerprojects will accept the award jointly with its EPC contractors who have been involved in the project at the plant.<br/>
The NSCI award is given by the labour ministry to applaud organisations that demonstrate consistent and meritorious practices in occupational safety and health. Ratul Puri, chairman, Hindustan Power, said that winning the award for second consecutive year is a testimony of the stringent and non-negotiable EHS practices followed by the company. According to him, sustainable results are only possible if the highest standards of safety are maintained and focus is placed on the principle of zero incidents. "The construction power projects are very long drawn and specialized. Hence it is necessary to introduce best-in-class safety practices in every area of the construction and commissioning,” he said.<br/>
Puri highlighted the fact that this is the only project in the country to have been bestowed with a single window approval of safety. Sharing how the project achieved its zero-incident status, he stated “Our highly sophisticated plant construction and erection process through deployment of state of the art mechanized equipment, providing training to the workmen and the policy of conducting medical examination including vertigo test before recruiting workmen has allowed incident free work at the site.”<br/>
